    SLR = BASE + 0x518
    DEN = BASE + 0x51C
    LOCK = BASE + 0x520
    CR = BASE + 0x524
    AMSEL = BASE + 0x528
    PCTL = BASE + 0x52C
    ADCCTL = BASE + 0x530
    MACTL = BASE + 0x534
