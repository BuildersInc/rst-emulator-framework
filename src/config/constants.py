    PERIPHID5 = BASE + 0xFD4
    PERIPHID6 = BASE + 0xFD8
    PERIPHID7 = BASE + 0xFDC
    PERIPHID0 = BASE + 0xFE0
    PERIPHID1 = BASE + 0xFE4
